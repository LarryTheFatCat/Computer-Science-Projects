# You can import external libraries
import math

# Print function allows you to enter text to print out on terminal
print("Hello, World")

# Using the math library that was imported we can then use factorial
# as a way to take the factorial of a number
print(math.factorial(52))

# Converts radians to degrees
print(math.degrees(60))

# Converts degrees to radians
print(math.radians(60))

# Prints the cosine angle of the input
print(math.cos(60))

# Checks whether or not the resultant is a number or not 
print(math.isnan(10))

# Hypotenuse of Input, uses coordinates x,y
print(math.hypot(1,2))

# Power of a function 
print(math.pow(3,2))
